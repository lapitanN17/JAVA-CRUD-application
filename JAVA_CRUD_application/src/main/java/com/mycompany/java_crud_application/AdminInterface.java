    public void loadUserData() throws SQLException {
        User_DataAccessObject userDAO = new User_DataAccessObject();
        List<User> users = userDAO.getUsers();
        
        String[][] data = new String[users.size()][5];
        for (int i = 0; i < users.size(); i++) {
            data[i][0] = String.valueOf(users.get(i).getId());
            data[i][1] = users.get(i).getName();
            data[i][2] = users.get(i).getEmailAddress();
            data[i][3] = users.get(i).getAge();
            data[i][4] = users.get(i).getCountry();
        }
        
        String[] columnName = {"ID", "Name", "Email Address", "Age", "Country"};
        DefaultTableModel tableModel = new DefaultTableModel(data, columnName) {
            @Override
            public boolean isCellEditable(int row, int column) {
               return false;
            }
        };

        usersTable.setModel(tableModel);
    }


