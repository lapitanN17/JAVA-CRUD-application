    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String inputName = nameTextField.getText();
        String selectedAge = ageComboBox.getSelectedItem().toString();
        String inputEmail = emailAddressTextField.getText();
        String inputPassword = String.valueOf(passwordTextField.getPassword());
        String selectedCountry = countryComboBox.getSelectedItem().toString();
        
        if (inputName.isBlank() || inputEmail.isBlank() || inputPassword.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!inputEmail.endsWith("@email.com") && !inputEmail.endsWith("@gmail.com")) {
            JOptionPane.showMessageDialog(this, "Invalid Email Address!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (inputPassword.length() < 8) {
            JOptionPane.showMessageDialog(this, "Password must be at least 8 characters!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        User newUser = new User(0, inputName, selectedAge, inputEmail, inputPassword, selectedCountry);
        try {
            User_DataAccessObject userDAO = new User_DataAccessObject();
            int res = userDAO.createUser(newUser);
            
            if (res == 1) {
                JOptionPane.showMessageDialog(this, "User added successfully!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                this.clearForm();
                loadUserData();
            } else {
                JOptionPane.showMessageDialog(this, "Error Occurred while inserting", "ERROR", JOptionPane.ERROR_MESSAGE);
                System.out.println("Debug 1");
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occurred while inserting", "ERROR", JOptionPane.ERROR_MESSAGE);
            System.out.println("Debug 2");
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if (selectedUserId != -1) {
            int confirmation = JOptionPane.showConfirmDialog(this,
                    "Are you sure you want to delete this user?",
                    "Confirmation",
                    JOptionPane.YES_NO_OPTION
            );
            
            if (confirmation == JOptionPane.YES_OPTION) {
                try {
                    User_DataAccessObject userDAO = new User_DataAccessObject();
                    userDAO.deleteUser(selectedUserId);
                    JOptionPane.showMessageDialog(this, "User deleted successfully!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    this.clearForm();
                    loadUserData();
                    selectedUserId = -1;
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "An error occurred while deleting!", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No user selected to delete!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void usersTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usersTableMouseClicked
        // TODO add your handling code here:
        int selectedRow = usersTable.getSelectedRow();
        
        if (selectedRow != -1) {
            selectedUserId = Integer.parseInt(usersTable.getValueAt(selectedRow, 0).toString());
            nameTextField.setText(usersTable.getValueAt(selectedRow, 1).toString());
            emailAddressTextField.setText(usersTable.getValueAt(selectedRow, 2).toString());
            ageComboBox.setSelectedItem(usersTable.getValueAt(selectedRow, 3).toString());
            countryComboBox.setSelectedItem(usersTable.getValueAt(selectedRow, 4).toString());
        }
    }//GEN-LAST:event_usersTableMouseClicked

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String name = nameTextField.getText();
        String selectedAge = ageComboBox.getSelectedItem().toString();
        String email = emailAddressTextField.getText();
        String inputPassword = String.valueOf(passwordTextField.getPassword());
        String selectedCountry = countryComboBox.getSelectedItem().toString();
        
        try {
            if (selectedUserId != -1) {
                User updateUserInfo = new User(selectedUserId, name, email, selectedAge, inputPassword, selectedCountry);
                User_DataAccessObject userDAO = new User_DataAccessObject();
                int res = userDAO.updateUser(updateUserInfo);

                if (res == 1) {
                    JOptionPane.showMessageDialog(this, "User updated successfully!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    this.clearForm();
                    loadUserData();
                    selectedUserId = -1;
                } else {
                    JOptionPane.showMessageDialog(this, "Error Occurred while updating", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error Occurred while updating", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Occurred while updating", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

